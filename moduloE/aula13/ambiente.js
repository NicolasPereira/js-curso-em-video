//Exemplo laço for
var c = 1
console.log('Vai Começar - For')
for(c = 1; c <=10; c++){
    console.log(c)
}
console.log('FIM -For')

//Exemplo laço While
var j = 1
console.log('Vai Começar - While')
while(j <= 10){
    console.log(j)
    j++
}
console.log('FIM - While')

//Exemplo laço Do While
var k = 1
console.log('Vai começar - Do While')
do{
    console.log(k)
    k++
}while(k <=10)
console.log('FIM - Do While')